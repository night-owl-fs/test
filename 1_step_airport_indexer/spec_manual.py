# expand to source filenames and write spec jsons
for ap, data in specs_map.items():
    sz = src_zoom.get(ap, 18)
    spec = {'zoom': sz, 'merc_zoom': None, 'tile_size':256, 'groups':[]}
    for zm, groups in data.items():
        merc = int(zm[1:])
        # map Z13->merc 9 etc
        merc_zoom = {13:9,14:9,15:9,16:11,17:13,18:13}[int(zm[1:])]
        spec['merc_zoom'] = merc_zoom
        gi = 0
        for group in groups:
            gi += 1
            files = []
            scale = 2 ** (sz - merc_zoom)
            for (mx,my) in group:
                sx0 = mx * scale
                sy0 = my * scale
                for sx in range(sx0, sx0 + scale):
                    for sy in range(sy0, sy0 + scale):
                        files.append(f"{sx}_{sy}.png")
            name = f"{ap}_M{merc_zoom}_G{gi}"
            spec['groups'].append({'name': name, 'files': files, 'output_tiff': f"{name}.tif"})
    out_path = os.path.join(out_dir, f"{ap}_spec.json")
    with open(out_path,'w',encoding='utf-8') as fh:
        json.dump(spec, fh, indent=2)
    print('Wrote', out_path)

print('Done')
